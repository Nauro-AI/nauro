"""Tests for the Claude Code CLAUDE.md bridge.

The bridge is an auto-written, Nauro-owned, thin CLAUDE.md whose only
context-visible content is an ``@AGENTS.md`` import - the one artifact that
makes Nauro's generated AGENTS.md load in an otherwise-unwired Claude Code
session. These tests pin the pure detector (fence/inline-span/BOM aware and
ownership by the exact marker line), the write/keep/advise/refuse/failure
policy, the byte-preserving ownership-gated teardown, and the wiring into the
shared AGENTS.md-regen seam (setup / adopt / sync / init), including the
no-oscillation guarantee against the legacy block cleanup.

The expected bridge file bytes are written out as a literal here on purpose:
any production wording or newline change must fail this suite, not silently
agree with an imported constant.
"""

from __future__ import annotations

import os
from pathlib import Path

import pytest
from typer.testing import CliRunner

from nauro.cli.integrations.claude_bridge import (
    BRIDGE_IMPORT,
    BridgeState,
    detect_bridge_state,
    ensure_claude_bridge,
    remove_claude_bridge,
)
from nauro.cli.integrations.legacy import CLAUDE_MD_END, CLAUDE_MD_START, _remove_claude_md
from nauro.cli.integrations.outcomes import BridgeKind
from nauro.cli.main import app
from nauro.constants import CLAUDE_BRIDGE_MARKER, CLAUDE_MD
from nauro.mcp.tools import tool_propose_decision
from nauro.store.registry import register_project_v2
from nauro.store.repo_config import save_repo_config
from nauro.templates.scaffolds import scaffold_project_store

runner = CliRunner()

# The exact bytes the bridge writes, spelled out (not imported) so a wording or
# newline drift in production fails here.
EXPECTED_BRIDGE = (
    "<!-- nauro-claude-bridge: auto-generated by Nauro. Imports AGENTS.md so "
    "Claude Code loads the shared project context (Claude Code does not read "
    "AGENTS.md natively). Managed by nauro; remove with 'nauro adopt --remove'. -->\n"
    "@AGENTS.md\n"
)
MARKER_LINE = EXPECTED_BRIDGE.splitlines()[0]


def _repo(tmp_path: Path) -> Path:
    repo = tmp_path / "repo"
    repo.mkdir()
    return repo


def _write(repo: Path, content: str) -> None:
    (repo / CLAUDE_MD).write_text(content, encoding="utf-8")


