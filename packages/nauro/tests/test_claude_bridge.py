def test_setup_claude_code_twice_keeps_bridge_stable(tmp_path: Path, monkeypatch):
    repo = tmp_path / "repo"
    repo.mkdir()
    pid, store = register_project_v2("proj", [repo])
    scaffold_project_store("proj", store)
    save_repo_config(repo, {"mode": "local", "id": pid, "name": "proj"})
    monkeypatch.chdir(repo)

    first = runner.invoke(app, ["setup", "claude-code"])
    assert first.exit_code == 0
    bridge_bytes = (repo / CLAUDE_MD).read_bytes()

    second = runner.invoke(app, ["setup", "claude-code"])
    assert second.exit_code == 0

    # The bridge survives a second run byte-for-byte, and the transcript is
    # stable (no oscillation between legacy cleanup and the bridge write).
    assert (repo / CLAUDE_MD).read_bytes() == bridge_bytes
    assert second.stdout == first.stdout


def test_legacy_block_stripped_then_treated_as_foreign_unbridged(tmp_path: Path, monkeypatch):
    """A CLAUDE.md carrying a legacy block keeps its remainder, advised not clobbered."""
    repo = tmp_path / "repo"
    repo.mkdir()
